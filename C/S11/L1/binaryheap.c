void maxheapify(int arr[],int n,int i)
{
    int largest=i;
    int left=2*i+1;
    int right=2*i+2;
    if(left<n && arr[left]>arr[largest])
    {
        largest=left;
    }
    if(right<n && arr[largest]<arr[right])
    {
        largest=right;
    }
    if(i!=largest)
    {
        int t=arr[i];
        arr[i]=arr[largest];
        arr[largest]=t;
        maxheapify(arr,n,largest);
    }
}
